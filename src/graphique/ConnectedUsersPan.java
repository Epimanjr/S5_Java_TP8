    private void mettreAJourListe(String[] tab) {
        // Modification de la liste
        jList1.setModel(new javax.swing.AbstractListModel() {
            String[] strings = tab;

            @Override
            public int getSize() {
                return strings.length;
            }

            @Override
            public Object getElementAt(int i) {
                if (i >= getSize()) {
                    return null;
                } else {
                    try {
                        Object o = strings[i];
                        return o;
                    } catch (java.lang.ArrayIndexOutOfBoundsException e) {
                        // e.printStackTrace();
                        return null;
                    }
                }

            }

        });
    }
